pub fn my_sqrt(x: i32) -> i32 {
        if x < 0 {panic!("negtive num is not allowed");}
        if x == 0 {return 0;}
        let mut x = x as f64;
        let mut cur = 1_f64;
        loop {
            let pre = cur;
            cur = (cur + x / cur) / 2_f64;
            println!("{},{}", cur, (cur-pre).abs());
            if (cur-pre).abs() < 1e-8 {
                return cur as i32;
            }
        }
    }
