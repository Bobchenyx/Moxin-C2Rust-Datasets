void merge(int* A, int ASize, int m, int* B, int BSize, int n){
int i=m-1,j=n-1,k=ASize-1;
while(i>=0&&j>=0)
{
    if(A[i]>B[j]) 
    {
        A[k]=A[i];
        i--;
        k--;
    }
    else{
        A[k]=B[j];
        j--;
        k--;
    }
}
while(i>=0)
{
        A[k]=A[i];
        i--;
        k--;
}
while(j>=0)
{
        A[k]=B[j];
        j--;
        k--;
}
}